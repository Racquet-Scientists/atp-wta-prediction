stats_name = function(stats) {
  player_names = str_split(stats$name, " ")
  n.obs <- sapply(player_names, length)
  seq.max <- seq_len(max(n.obs))
  for (i in which(n.obs > 3)) {
    stats[nrow(stats)+1, ] = stats[i, ]
    player_names[length(player_names)+1] = player_names[i]
    n.obs = c(n.obs, 3)
  }
  for (i in which(n.obs == 3)) {
    stats[nrow(stats)+1, ] = stats[i, ]
    player_names[length(player_names)+1] = player_names[i]
    n.obs = c(n.obs, 2)
  }
  matname <- t(sapply(player_names, "[", i = seq.max))  # alternative method: matname = plyr::ldply(player, rbind)
  lastname = matname[cbind(c(1:nrow(matname)), n.obs)]
  firstname_initial = paste(substr(matname[, 1], 1, 1), ".", sep = "")
  stats_name = paste(lastname, firstname_initial, sep = " ")
  stats$name = stats_name
  return(stats)
}

results_name = function(player) {
  player_names = str_split(player, " ")
  n.obs <- sapply(player_names, length)
  seq.max <- seq_len(max(n.obs))
  matname <- t(sapply(player_names, "[", i = seq.max))  # alternative method: matname = plyr::ldply(player, rbind)
  lastname = matname[cbind(c(1:length(player)), 1)]
  firstname = matname[cbind(c(1:length(player)), n.obs)]
  firstname_initial = substr(firstname, 1, 2)
  
  lastname = str_split(lastname, "-")
  n.obs <- sapply(lastname, length)
  seq.max <- seq_len(max(n.obs))
  matname <- t(sapply(lastname, "[", i = seq.max))
  lastname = matname[cbind(c(1:length(player)), 1)]
  results_name = paste(lastname, firstname_initial, sep = " ")
  return(results_name)
}

results_stats = function(Results, player, stats) {
  player_stats = matrix(nrow = nrow(Results), ncol = 1)
  for (i in 1:nrow(Results)) {
    n = pmatch(player[i], stats$name)
    player_stats[i] = stats$value[n]
  }
  return(player_stats)
}

